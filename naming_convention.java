public class naming_convention {
    public static void main(String[] args) {
        System.out.println("Naming convention in Java");
    }
}

/*
 *  camel casing
 *  class and interfaces name should be capitalized - Human.
 * variables and methods should be in lower case  -marks,name,age,get(),
 * constants should be in upper case - PIE,BRAND.
 * names with two letters - first should be smaller case and another must be in capitalized.  - camelcasing - getValues(),employeeAge.
 * snake casing - separate the words by underscore(_) - number_of_employees
 * a class name with two words should be capitalized to both - NewStudent
 * */
